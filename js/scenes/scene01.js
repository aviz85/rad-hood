  /* Free-command interception: self-look (Red heard the intro) and the
     dead-phone call gag. Everything else falls through to the engine. */
  onCommand(g, verb, noun) {
    /* matchNoun normalizes both sides (final letters, apostrophes, he/bet/lamed
       prefixes), so forms like "בעצמך" or "קפוצ'ון" match correctly. */
    if (verb === 'look' && noun &&
        RHParser.matchNoun(noun, ['עצמי', 'עצמך', 'עצמה', 'כיפה', 'אני', "קפוצ'ון"])) {
      if (!g.flag('selfLook01')) {
        g.flag('selfLook01', 1);
        g.sayAs('כיפה', 'שמעתי הכל, אתה יודע. גם את הקטע על הלב זהב. תתבע אותי.');
      } else {
        g.say('כיפה. קפוצ\'ון אדום, ביטחון עצמי בגודל של בלוק, וסבלנות שנגמרה עוד לפני שהתחלנו.');
      }
      return true;
    }
    if (verb === 'call') {
      g.say('הסוללה מתה, וגם ככה אמא במרחק שני מטרים. תנסי טכנולוגיה עתיקה: דיבור.');
      return true;
    }
    return false;
  },
