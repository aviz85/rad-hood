/* scene06 — Forest clearing: Zevik's grill stand. The heart of the game.
   First talk: the five-beat confessional (story.md) -> +5 p_wolfTalk, wolfTold.
   Give mushrooms: happy grill cutscene -> +25 p_feedWolf, wolfFriend, note.
   Fire: torch only after friendship -> +10 p_torch.
   Dosage per review.md: one apology and one lawyer mention per conversation;
   the menu closer and Red's note deflection are verbatim and non-negotiable. */
(function () {
  /* Shared by "take fire" and "use fire" — the torch gate. */
  function takeTorch(g) {
    if (!g.flag('wolfFriend')) {
      g.say('לקחת אש ממדורה של זאב זר? תרבות, איפה.');
      return;
    }
    if (g.flag('gotTorch')) {
      g.say('לפיד אחד כבר דולק אצלך. שניים זה לא תאורה, זה שריפת יער עם שאיפות.');
      return;
    }
    g.say('שלפת מהמדורה ענף עבה ובוער. לפיד אמיתי: חם, נאמן, ולא דורש סוללה. שמעת, טלפון?');
    g.sayAs('זאביק', 'זהירות עם זה... ותחזרי לספר לי מה ראית שם בפנים. או אל תספרי. לפעמים עדיף לא.');
    g.give('torch');                 // g.give plays the pickup sfx itself
    g.addScore(10, 'p_torch');
    g.flag('gotTorch', 1);
  }

  RH.registerScene({
    id: 'scene06',
    name: 'קרחת היער — הגריל של זאביק',
    art: 'art06',
    music: 'm_grill',
    horizon: 116,

    look: 'קרחת יער עם עמדת גריל אחת, מדורה מסודרת להפליא, שלט שמבטיח "100% צמחוני" — ומאחורי הדלפק הזאב הכי עצוב שתראי היום. אין תור. אף פעם אין תור.',

    enter(g) {
      if (!g.flag('seen06')) {
        g.flag('seen06', 1);
        g.say('קרחת יער. עמדת גריל מאולתרת, מדורה קטנה, שלט תפריט מצויר ביד — ומאחורי הדלפק: זאב. בסינר.');
        g.say('הוא מצחצח רשת שנקייה ממילא ונאנח. זאת האנחה השלישית מאז שנכנסת. אנחנו סופרים.');
        g.sayAs('כיפה', 'מה נסגר... זאב עם דוכן גריל. או שזאת מלכודת, או שזה היום הכי מעניין בחיים שלי.');
      }
    },

    hotspots: [
      {
        id: 'wolf',
        names: ['זאב', 'זאביק', 'זאב צמחוני'],
        rect: [128, 90, 48, 84],
        sprite: 'wolf',
        pos: [152, 172],
        look(g) {
          if (g.flag('wolfFriend')) {
            g.say('זאביק ליד הגריל, סינר מהודק, חיוך של מי שיש לו סוף-סוף לקוחה קבועה. קשה להאמין שזה הנבל מהפודקאסט.');
          } else {
            g.say('זאב אפור ענקי עם סינר גריל ומבט של מודעת אימוץ. שיניים של טורף, עמידה של מתנצל. משהו פה לא מסתדר עם האגדה.');
          }
        },
        talk(g) {
          if (!g.flag('wolfTold')) {
            g.sayAs('זאביק',
              'אה... שלום. סליחה. את בטח רוצה לברוח עכשיו, נכון? זה בסדר... כולם בורחים. אני אחכה שתסיימי לצרוח.');
            g.sayAs('כיפה', 'אחי, אני משכונה שבה הדבר הכי מפחיד זה הוועד בית. דבר.');
            g.sayAs('זאביק',
              'עשרים שנה אני צמחוני. עשרים שנה. ועדיין, ילדה אחת רואה אותי ביער ולמחרת יש עליי פודקאסט פשע.');
            g.sayAs('זאביק',
              'פורטובלו... את יודעת מה זה כשפורטובלו פוגש רשת לוהטת? הרחש הזה... טסססס...',
              'כובע חום, נקודות בהירות — רק אלה! האדומות זה בית חולים, הזוהרות זה הלוויה. תסמכי על זאב שאוכל רק סלט.');
            g.sayAs('זאביק',
              'ואם את בדרך לסבתא שלך... אני לא אוהב לדבר רע על אף אחד. באמת שלא. אבל היא לא מה שנדמה לך. תיזהרי מהריבה. *במיוחד* מהריבה.');
            g.addScore(5, 'p_wolfTalk');
            g.flag('wolfTold', 1);
          } else if (!g.flag('wolfFriend')) {
            if (!g.flag('wolfReminded')) {
              g.flag('wolfReminded', 1);
              g.sayAs('זאביק',
                'אם במקרה... רק אם יוצא לך... שדה הפטריות נמצא במעלה השביל. כובע חום, נקודות בהירות. אני אחכה פה. אני תמיד פה.');
              g.sayAs('כיפה', 'אוקיי, רשמתי: כובע חום, נקודות בהירות. אם אני נתקלת — אולי. בלי הבטחות.');
            } else {
              g.sayAs('זאביק',
                'התביעה נגד האחים גרים עדיין בערכאות. עו"ד שועלי אומר שיש לנו תיק חזק. הוא גם אומר שאני לא אמור לדבר על זה, אז... לא דיברנו.');
              g.sayAs('כיפה', 'וואלה, זאב עם עורך דין. היער הזה מסודר יותר מהשכונה.');
            }
          } else {
            g.sayAs('זאביק',
              'החלום שלי? מסעדה קטנה. שש שולחנות. גריל פטריות, חומוס ביתי, ואף אחד לא בורח כשאני מגיש... סתם, חלומות. מי ייכנס למסעדה של זאב.');
            g.sayAs('כיפה', 'אני. שולחן ליד החלון, בלי תור. ושיהיה לך ברור — לאן שאני נכנסת, כל השכונה נכנסת אחריי.');
          }
        },
        give: {
          mushrooms(g) {
            /* Repeat gift after the friendship cutscene (mushrooms are
               re-pickable in scene07) — accept warmly without replaying the
               first-time cutscene or re-offering the note and the torch. */
            if (g.flag('wolfFriend')) {
              g.sayAs('זאביק', 'עוד פטריות? בשבילי? את... את מפנקת אותי. ישר לרשת. ספיישל הערב על שמך — והשולחן הכי טוב שמור לך. תמיד.');
              g.say('הוא מניח אותן על הגריל ביראת קודש. טסססס. יש לקוחות קבועות, ויש מייסדות.');
              g.takeItem('mushrooms');
              return;
            }
            g.sayAs('זאביק',
              'זה... זה בשבילי? באמת בשבילי? אף אחד לא הביא לי כלום מאז... אף פעם. שבי, שבי! אני מדליק את הגריל ואת לא זזה עד שטעמת!');
            g.say('הגריל נדלק. הפורטובלו פוגש את הרשת. טסססס. הריח מתפשט על פני שלוש סצנות לפחות.');
            g.sayAs('כיפה', 'אחי... זה ריח של פורטובלו על האש? סוף הדרך. קח חמש כוכבים בגוגל, מובטח.');
            g.sayAs('זאביק',
              'קחי את זה. פתק ממני. אם מישהו ביער עושה לך בעיות — תראי לו. מסתבר שהשם הרע שלי... לפעמים הוא שימושי.');
            g.sayAs('כיפה', 'נשמה אתה, זאביק. ...מה? לא בכיתי. נכנס לי עשן של פורטובלו לעין.');
            g.sayAs('זאביק',
              'ואם את ממשיכה פנימה ליער — קחי אש מהמדורה. שם בפנים חשוך, ו... תצטרכי אור. אל תשאלי איך אני יודע.');
            g.takeItem('mushrooms');
            g.give('note');          // g.give plays the pickup sfx itself
            g.addScore(25, 'p_feedWolf');
            g.flag('wolfFriend', 1);
          },
          note(g) {
            g.sayAs('זאביק', 'לא-לא, זה שלך. כתבתי אותו בעט הטוב, עם החתימה המפחידה. שמרי עליו... זה הדבר הכי שימושי שהשם שלי עשה אי פעם.');
          },
          basket(g) {
            g.sayAs('זאביק', 'מאפים? לא, לא... זה לסבתא שלך. אני לא נוגע באוכל של אחרים. מספיק שמאשימים אותי בדברים שלא אכלתי.');
          },
          jam(g) {
            g.sayAs('זאביק', 'תרחיקי... תרחיקי את זה ממני. ריח של תות עם משהו מתחת... תבטיחי לי שאת לא טועמת מזה. תבטיחי.');
            g.say('זאב, טורף-על לשעבר, נסוג שני צעדים מצנצנת ריבה. תרשמי את זה בתיק הראיות.');
          },
          '*': function (g) {
            g.sayAs('זאביק', 'זה מאוד נדיב, אבל... אני בסדר. באמת. הכל בסדר אצלי.');
            g.say('הוא לא בסדר. גם הגריל יודע.');
          },
        },
        show: {
          note(g) {
            g.sayAs('זאביק', 'כן... זה שלי. החתימה יצאה טוב, נכון? עבדתי על הזנב המפחיד בסוף.');
          },
          '*': function (g) {
            g.sayAs('זאביק', 'יפה מאוד... מה זה? לא משנה. אם זה חשוב לך, זה יפה.');
          },
        },
        take(g) {
          g.say('לקחת את הזאב? הוא ישמח לבוא, אבל הוא מאה קילו של עצב ועוד עשרה של סינר. הסלסלה מוותרת.');
        },
        eat(g) {
          g.say('לאכול את הזאב? היפוך התפקידים הזה ישבור את האגדה, את זאביק, וכנראה גם אותך. הוא תשעים אחוז עצב, עשרה אחוז סינר.');
        },
        push(g) {
          g.sayAs('זאביק', 'אוי. סליחה. עמדתי באמצע? אני אזוז...');
          g.say('דחפת זאב, והזאב התנצל. קחי רגע לתת לזה לשקוע.');
        },
        knock(g) {
          g.say('נקשת לזאביק על הכתף. הוא קפץ מטר באוויר ושמט את המלקחיים. הזאב הגדול והרע, גבירותיי.');
        },
      },

      {
        id: 'grill',
        names: ['גריל', 'מנגל', 'דוכן', 'עמדה', 'עמדת גריל', 'דלפק', 'רשת'],
        rect: [186, 118, 62, 52],
        look: 'עמדת גריל מאולתרת, מצוחצחת ברמה של מסעדה עם כוכב. אין לקוחות, אבל יש סטנדרטים — וזה, אחותי, כל הסיפור של זאביק.',
        take(g) {
          g.say('להרים גריל שלם לסלסלה? זאת הפרנסה של זאביק — הפרנסה היחידה ביער עם רישיון עסק. עזבי.');
        },
        open(g) {
          g.say('פתחת את מכסה הגריל. בפנים: רשת נקייה ותקווה. סגרת בעדינות.');
        },
        eat(g) {
          if (g.flag('wolfFriend')) {
            g.say('כבר טעמת את הפורטובלו הכי טוב ביער. ללקק את הרשת אחרי זה — זה כבר בינך לבין המצפון שלך. והמצפון אמר לא.');
          } else {
            g.say('הרשת ריקה ומבריקה. אין מה לאכול חוץ מהשתקפות של עצמך, וזאת מנה שכבר יש לך בבית.');
          }
        },
        use: {
          mushrooms(g) {
            g.say('הגריל של זאביק, הטקס של זאביק. תני לו את הפטריות — תני למקצוען לעבוד.');
          },
          '*': function (g) {
            if (g.flag('wolfFriend')) {
              g.say('הגריל עבד היום בשבילך, והיה מושלם. אל תהיי גרגרנית — זה נגמר רע במשחקים כאלה.');
            } else {
              g.say('לגעת במנגל של מישהו אחר בלי הזמנה? בשכונה זה עילה לריב. ביער, מסתבר, אותם חוקים בדיוק.');
            }
          },
        },
        give: {
          mushrooms(g) {
            g.say('הגריל לא לוקח הזמנות. הזאב עם הסינר לידו — הוא ההנהלה. תני לו.');
          },
          '*': 'הגריל שקל את ההצעה ונשאר גריל. תנסי את הזאב, הוא הקשוב מבין השניים.',
        },
        push(g) {
          g.say('הגריל התנדנד. זאביק שאג... סליחה, מלמל "בעדינות בבקשה". איזה טורף.');
        },
        knock(g) {
          g.say('דפקת על הדלפק כמו לקוחה עם תלונה. זאביק הופיע תוך שנייה עם מלקחיים ותקווה בעיניים. עכשיו תזמיני משהו, אכזרית.');
        },
      },

      {
        id: 'fire',
        names: ['מדורה', 'אש', 'לפיד', 'גחלים', 'להבה'],
        rect: [196, 152, 22, 18],
        look: 'מדורה קטנה ומסודרת, גחלים במרחקים שווים בדיוק. זאביק כנראה היצור היחיד ביער שמסדר אש לפי פנג שוואי.',
        take: takeTorch,
        use: {
          torch(g) {
            if (g.has('torch')) {
              g.say('הלפיד שלך כבר דולק. להדליק אש באש זה תרגיל פילוסופי, ואנחנו במשחק הרפתקאות, לא בחוג.');
            } else {
              takeTorch(g);
            }
          },
          '*': takeTorch,
        },
        eat(g) {
          g.say('לאכול אש? אפילו במשחק שבו טועמים פטריות מהרצפה יש גבול. הנה, מצאת אותו.');
        },
        push(g) {
          g.say('פיזרת קצת גחלים עם הנעל. זאביק סידר אותן בחזרה לפי הפנג שוואי עוד לפני שסיימת להתנצל.');
        },
        open(g) {
          g.say('אש היא לא צנצנת. היא כבר הדבר הכי פתוח שיש פה.');
        },
      },

      {
        id: 'menu',
        names: ['תפריט', 'שלט', 'שלט תפריט', 'מנות'],
        rect: [98, 108, 26, 40],
        look: 'שלט תפריט מצויר ביד: שיפודי פטריות, פטריות על האש, ופטריות בהפתעה. ההפתעה: עוד פטריות.',
        read(g) {
          g.say('"הזאב הטוב — 100% צמחוני". תפריט: פורטובלו על האש. שיפוד פורטובלו. פורטובלו בפיתה. קינוח: פורטובלו.');
          g.say('בתחתית, באותיות קטנות: "אין שירות משלוחים. ניסיתי. הם צרחו."');
        },
        take(g) {
          g.say('זאביק צבע את השלט הזה בשלוש שכבות ובאהבה. עזבי לו את היצירה — ממילא כל התפריט נכנס במילה אחת.');
        },
        eat(g) {
          g.say('הפטריות שעל השלט מצוירות. ההבדל בין פטרייה מצוירת לפטרייה אמיתית עוד יתברר לך כקריטי. רמז: שדה הפטריות.');
        },
        push(g) {
          g.say('יישרת את השלט. הוא היה ישר. עכשיו הוא עקום. זאביק חייך בנימוס ומת קצת מבפנים.');
        },
        knock(g) {
          g.say('דפקת על שלט התפריט. אף מנה לא ענתה. כולן פורטובלו וכולן עסוקות.');
        },
      },
    ],

    exits: {
      left: { to: 'scene05' },
      right: { to: 'scene08' },
    },

    onCommand(g, verb, noun) {
      /* Eating the portobello meant for the grill — a spec death (jokes.md #16),
         landed here because doing it in front of Zevik's stand is peak tragedy. */
      if (verb === 'eat' && g.has('mushrooms') &&
          (noun.indexOf('פטרי') !== -1 || noun.indexOf('פורטובלו') !== -1)) {
        g.die('הפורטובלו היה מיועד לגריל של זאב מומחה, לא לנגיסה קרה בעמידה. הקיבה שלך הגישה התפטרות. התקבלה.');
        return true;
      }
      return false;
    },
  });
})();
