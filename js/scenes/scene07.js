/* scene07 — The mushroom field. The `mushrooms` fetch + classic Sierra deaths.
   Emotional beat (story.md): Red, who "doesn't care", is out here picking
   mushrooms for a wolf. The Narrator notices.
   Key beats (per design/review.md):
   - All three eat-deaths use jokes.md #13-15 verbatim. Eating even the CORRECT
     (brown) mushrooms kills — that subversion is the scene's biggest laugh.
   - The pick-for-the-wolf beat is the Narrator/Red one-two exactly as written;
     Red's denial closes it.
   - Scarecrow look merges the cynical take + the embroidered apron (Grandma
     foreshadow) + the moving hat. */
RH.registerScene({
  id: 'scene07',
  name: 'שדה הפטריות',
  art: 'art07',
  music: 'm_forest',
  horizon: 125,

  look:
    'קרחת יער שהשמש החליטה לדלג עליה: שלוש ערוגות פטריות — אדומות, זוהרות, חומות — ודחליל ישן שמחזיק את כל האווירה לבד. ' +
    'הוא לא זז. בינתיים.',

  enter(g) {
    // Idempotent first-entry beat: the menu of bad decisions, Red covers the chill.
    if (!g.flag('seen07')) {
      g.flag('seen07', 1);
      g.say(
        'שדה הפטריות. אדומות, זוהרות, חומות — תפריט שלם של החלטות גרועות, מסודר בערוגות.'
      );
      g.sayAs(
        'כיפה',
        'חושך באמצע היום, ודחליל שעומד כאילו משלמים לו. סבבה. עובדים מהר ועפים מפה.'
      );
    }
  },

  hotspots: [
    {
      id: 'redshrooms',
      names: ['פטריות אדומות', 'פטרייה אדומה', 'אדומות', 'פטריות עם נקודות'],
      rect: [56, 148, 34, 22],
      look:
        'פטריות אדומות עם נקודות לבנות, יפות כמו מהאגדות. באגדות, נזכיר, גם סבתות הן בחורות נחמדות.',
      take(g) {
        // Red's refusal — story.md verbatim.
        g.sayAs(
          'כיפה',
          'אדומות עם נקודות? חיים שלי, ככה נראית פטרייה שמצלמים לספרי אזהרה.'
        );
      },
      eat(g) {
        // Death — jokes.md #13, verbatim.
        g.die(
          'פטרייה אדומה עם נקודות לבנות. גם בציור של ילד בגן זה סימן אזהרה בינלאומי. כיפה הבינה את זה — שתי דקות מאוחר מדי.'
        );
      },
      talk: 'דיברת עם פטרייה רעילה. היא שמרה על שתיקה — בתחום שלה, מי שמדברת אחרונה מנצחת.',
      push:
        'בעטת קלות באדומות. ענן דק של אבקה לבנה עלה באוויר. עצרי את הנשימה וקחי צעד אחורה. ברצינות.',
    },
    {
      id: 'glowshrooms',
      names: ['פטריות זוהרות', 'פטרייה זוהרת', 'זוהרות', 'פטריות ירוקות'],
      rect: [148, 140, 32, 20],
      look:
        'פטריות שזוהרות בירוק. שום דבר שזוהר מעצמו בטבע לא רוצה שתאכלי אותו — זה חוק ראשון בביולוגיה וגם בשכל ישר.',
      take(g) {
        // Red's refusal — story.md verbatim.
        g.sayAs(
          'כיפה',
          'היא זוהרת. אוכל לא אמור לזהור. גם לא בשכונה שלי, וזה אומר משהו.'
        );
      },
      eat(g) {
        // Death — jokes.md #14, verbatim.
        g.die(
          'הפטרייה זהרה בחושך. עכשיו גם את זוהרת — מלמעלה, עם כנפיים. סיירה הייתה גאה. ההורים שלך פחות.'
        );
      },
      talk: 'דיברת עם הפטריות הזוהרות. הזוהר הבהב פעמיים. אנחנו לא יודעים מה זה אומר ולא נשארים לברר.',
      push:
        'נגעת בקצה הנעל. הזוהר התחזק לרגע, כאילו נדלק לו תיאבון. צעד אחורה היה רעיון מצוין.',
    },
    {
      id: 'brownshrooms',
      names: ['פטריות חומות', 'פטרייה חומה', 'חומות', 'פורטובלו', 'פטריות'],
      rect: [238, 152, 38, 20],
      look:
        'פטריות חומות עם נקודות בהירות. כובע חום, נקודות בהירות... מוכר לך מאיפשהו? למישהו עצוב עם זנב זה היה מוכר.',
      take(g) {
        if (!g.flag('wolfTold')) {
          // No spec from Zevik yet — Red doesn't gamble (story.md verbatim).
          g.sayAs('כיפה', 'אין לי מושג מה אכיל פה ואני לא בונה על מזל.');
          return;
        }
        if (g.has('mushrooms')) {
          g.sayAs('כיפה', 'יש לי כבר מלוא החופן. זאביק אמר טריות, לא כמויות.');
          return;
        }
        g.give('mushrooms');
        g.addScore(15, 'p_shrooms');
        if (!g.flag('picked07')) {
          g.flag('picked07', 1);
          // The Narrator/Red one-two — review.md scene07 note 2, verbatim. Red closes.
          g.say('את קוטפת פטריות בשביל זאב. שאלות על מסלול הקריירה שלך — בסוף המשחק.');
          g.sayAs('כיפה', 'זה היה לי בדרך. נשבעת.');
        } else {
          g.say('קטפת עוד חופן פורטובלו. הערוגה לא שופטת. אני קצת.');
        }
      },
      eat(g) {
        // Death — even the correct mushrooms, raw off the ground. jokes.md #15, verbatim.
        g.die(
          'אכלת פטרייה אנונימית מרצפת יער. זאביק בוכה במרחק שתי סצנות — לא מעצב. מבושה מקצועית.'
        );
      },
      talk(g) {
        if (g.flag('wolfTold')) {
          g.sayAs('כיפה', 'אתן באות איתי. יש זאב אחד שהולך להתעלף מאושר.');
        } else {
          g.say(
            'דיברת עם הפטריות החומות. הן לא ענו — אבל מכל מה שצומח פה, הן לפחות נראות כמו הטיפוס השקט והאמין.'
          );
        }
      },
      push:
        'דחפת פטרייה חומה. היא התנדנדה וחזרה לעמוד. סחורה איכותית — מי שמבין, מבין.',
    },
    {
      id: 'scarecrow',
      names: ['דחליל', 'איש קש', 'קש', 'בובה'],
      rect: [96, 88, 30, 64],
      look:
        'דחליל ישן עם סוודר סרוג וסינר רקום ביד — למי ביער יש בכלל סבלנות לרקום סינר לדחליל? רגע — הכובע שלו זז עכשיו? אין רוח. אין. רוח.',
      talk(g) {
        g.sayAs('כיפה', 'אהלן, אחי. ראית פה סבתא עוברת?');
        g.say(
          'הדחליל לא ענה. הראש שלו, לעומת זאת, נטה סנטימטר לכיוון שלך. עכשיו תורך לא לענות.'
        );
      },
      take: 'מזכרת מהיער? קחי משהו שלא נתפר ביד של מישהי שתשים לב שהוא חסר.',
      push:
        'דחפת. הדחליל לא זז מילימטר — מי שתקעה אותו באדמה התכוונה שיישאר. הסינר, לעומת זאת, התיישר לבד.',
      knock: 'נקשת על עמוד הקש. מבפנים אין הד. יש האזנה.',
      eat: 'קש יבש בתיבול ייאוש. אפילו הפטריות הרעילות פה מציעות עסקה טובה יותר.',
      open: 'אין בו דלת. יש בו תפרים. ואת לא פותחת תפרים של מישהי שסופרת אותם.',
      photo:
        'הסוללה מתה. וגם אם לא — דחלילים כאלה תמיד יוצאים בתמונה צעד אחד קרוב יותר ממה שזכרת.',
      use: {
        torch(g) {
          g.say(
            'לשרוף את הדחליל? מצד אחד — בבקשה, עוד היום. מצד שני, מי שרקמה לו סינר תרגיש שהוא נעלם. מנמיכים את הלפיד וממשיכים בשקט.'
          );
        },
        '*': 'הדחליל לא צריך את זה. הדחליל לא צריך כלום. זה בדיוק מה שמלחיץ בו.',
      },
      give: {
        '*': 'הושטת יד עם מתנה לדחליל, ואז נזכרת שאת לא רוצה לראות אותו מושיט יד בחזרה.',
      },
    },
  ],

  exits: {
    down: { to: 'scene05' },
  },
});
