RH.registerScene({
  id: 'scene09',
  name: 'המערה',
  art: 'art09',
  music: 'm_danger',
  horizon: 120,

  look:
    'מערה. אור הלפיד מגרד את החושך ומגלה נטיפים, עטלפים, ארגז ריבה שאין לו שום סיבה להיות פה, ' +
    'וציורי קיר שמישהי השקיעה בהם. הרבה. אף אחד לא משקיע ככה במקום שהוא רק עובר בו.',

  enter(g) {
    // Torch gate — checked on EVERY entry. Without light Red turns around on the spot.
    if (!g.has('torch')) {
      g.sayAs('כיפה', 'חושך, אחותי. אני ערסית, לא עטלף. נחזור עם תאורה.');
      g.goto('scene08', 82, 132);
      return;
    }
    // Idempotent first-entry beat: one joke at the door, then the room goes quiet.
    if (!g.flag('seen09')) {
      g.flag('seen09', 1);
      g.say(
        'הלפיד דוחף את החושך צעד אחורה, והחושך — מורגש — סופר עד עשר וממתין.'
      );
      g.sayAs(
        'כיפה',
        'סבבה, נכנסנו. ...למה ריח של ריבה במערה? אחי, למה ריח של ריבה במערה.'
      );
    }
  },

  hotspots: [
    {
      id: 'key_glint',
      names: ['מפתח', 'נצנוץ', 'ברק', 'מתכת', 'מפתח חלוד'],
      rect: [252, 162, 14, 10],
      when: g => !g.has('rusty_key'),
      look:
        'משהו מתכתי מנצנץ באור הלפיד, ליד הקיר. במערות, מה שמנצנץ זה או אוצר או בעיה. לפעמים שניהם.',
      take(g) {
        g.takeItem('rusty_key');
        g.addScore(10, 'p_key');
        g.playSfx('pickup');
        g.sayAs(
          'כיפה',
          'מפתח! חלוד, מגעיל, מושלם. על הכיפאק. דברים חלודים פותחים תמיד את הדלתות הכי שוות.'
        );
      },
      push: 'דחפת את הנצנוץ. הוא נצנץ ממקום חדש. נסי פשוט לקחת אותו — פחות פילוסופי, יותר יעיל.',
      eat: 'לאכול מתכת חלודה? יש חיסון נגד טטנוס, אין חיסון נגד רעיונות כאלה.',
      knock: 'נקשת ליד הנצנוץ. הוא לא ענה. דברים קטנים ומתכתיים בדרך כלל לא.',
    },
    {
      id: 'jambox',
      names: ['ארגז', 'ארגז ריבה', 'ריבה', 'צנצנת', 'צנצנות', 'קופסה'],
      rect: [58, 138, 42, 32],
      look:
        "ארגז עץ עמוס צנצנות ריבה, מסודרות כמו תחמושת. על הצד חותמת: 'אצווה 7. לא לשיווק. את יודעת למה.'",
      read: 'התווית על הצנצנות: "ריבת סבתא — טעם אחד ואת בעננים. מילולית."',
      take(g) {
        if (g.has('jam')) {
          g.say('צנצנת אחת בסלסלה זה ראיה. שתיים זה כבר מלאי, ואת לא בעסקי הריבה. שתישאר.');
          return;
        }
        g.takeItem('jam');
        g.addScore(10, 'p_jam');
        g.playSfx('pickup');
        g.say(
          'לקחת צנצנת אחת. ארגז שלם. במערה. רחוק מכל חנות. לוגיסטיקה מרשימה למישהי שאומרת שהיא "רק מבשלת לנכדים".'
        );
        g.sayAs('כיפה', 'אני לוקחת אחת. לא לטעימה — להוכחה. שיהיה ברור לפרוטוקול.');
      },
      eat(g) {
        // Jam death — jokes.md #19, verbatim.
        g.die('ריבת סבתא, ישר מהצנצנת. הטעם: תות. הריח: שקדים מרים. הסוף: בערך עכשיו.');
      },
      open: 'הארגז כבר פתוח. מי שסידרה אותו רצתה שיראו את הסחורה — או שהיא פשוט בוטחת מאוד בחושך.',
      push:
        'דחפת את הארגז. הצנצנות השמיעו צליל זכוכית עדין, כמו פעמון. במערה הזאת גם הפעמונים נשמעים כמו אזהרה.',
      knock: 'נקשת על הארגז. עץ מלא. מי שבנתה אותו בנתה אותו להחזיק. את לא רוצה לדעת מה עוד היא בנתה ככה.',
      use: { '*': 'הארגז הזה כבר עושה את העבודה שלו: עומד במערה ומעלה שאלות. אל תפריעי לו.' },
    },
    {
      id: 'paintings',
      names: ['ציורים', 'ציור', 'ציורי קיר', 'ציורי מערה', 'קיר'],
      rect: [120, 60, 80, 44],
      look(g) {
        g.say('מישהו צייר סבתא עם מסור. אמנות.');
        g.say(
          'הציורים ישנים. שכבות-שכבות. מי שצייר אותם חזר לכאן הרבה פעמים... וכנראה לא בשביל האקוסטיקה.'
        );
      },
      use: {
        torch(g) {
          g.say(
            'קירבת את הלפיד. בשכבה התחתונה, הכי ישנה, מצויר זאב קטן שבורח. בשכבה העליונה הוא כבר לא בורח. הוא מצויר עם איקס.'
          );
          g.sayAs('כיפה', 'מסגרת. מישהי בנתה לזאביק תיק במשך שנים. הזוי.');
        },
        '*': 'הציורים שרדו פה דורות. הם יישרדו גם את הרעיון הזה.',
      },
      take: 'לקחת ציור קיר לוקחים עם הקיר. הקיר מחזיק מערה. נעזוב את זה בשלב התכנון.',
      read: 'אין מילים על הקיר. מי שציירה את זה אמרה הכל בתמונות, וזה החלק שהכי פחות מרגיע.',
      talk(g) {
        g.sayAs('כיפה', 'מי צייר אתכם, אה? שם, כתובת, מספר נעליים?');
        g.say('הציורים שתקו. עדים ביער הזה יודעים מתי לא משתלם לדבר.');
      },
      eat: 'לאכול קיר? יש מינרלים, אין שיניים שיחזיקו מעמד.',
      push: 'נשענת על הקיר. הקיר נשאר קיר. לפחות משהו פה יציב.',
    },
    {
      id: 'pawprints',
      names: ['עקבות', 'טביעות', 'טביעות רגליים', 'עקבות חתול', 'כפות'],
      rect: [180, 170, 40, 12],
      look(g) {
        // The scene's emotional beat — Red closes it, no joke after (review.md).
        g.say(
          'עקבות של חתול קטן. הולכות פנימה... ואז נגררות. בכיוון של הבית של סבתא.'
        );
        g.sayAs('כיפה', 'מתוק, נשמה, לאן לקחו אותך?');
      },
      take: 'עקבות לא לוקחים. עוקבים אחריהן. ההמשך שלהן — בכיוון של סבתא.',
      talk: 'דיברת אל עקבות. הן כבר אמרו את כל מה שהיה להן: מישהו קטן נכנס לפה ולא יצא לבד.',
      eat: 'אבק מערה זה לא מנה. גם לא מנת פתיחה.',
      push: 'אל תמרחי את הראיות. החתול עבד קשה להשאיר אותן.',
    },
    {
      id: 'bats',
      names: ['עטלפים', 'עטלף'],
      rect: [140, 30, 60, 24],
      look:
        'עטלפים. הם היחידים ביער שלא מפחדים מהזאב. הם מפחדים ממשהו אחר. תשאלי אותם ממה — הם נהיים שקטים מאוד.',
      talk(g) {
        g.sayAs('כיפה', 'אחים שלי, מי מסדר פה ארגזי ריבה? תנו כיוון.');
        g.say('העטלפים הצטופפו קצת יותר. זאת הייתה תשובה. לא טובה.');
      },
      take: 'לקחת עטלף הביתה? יש לך כבר סלסלה עם מאפים. לא מערבבים מחלקות.',
      eat: 'לא. גם לא ב-1989, גם לא במערה, גם לא בתור אתגר.',
      push: 'הם בתקרה, את ברצפה. הפיזיקה הצביעה נגד ההצעה.',
      knock(g) {
        g.say('נקשת על הקיר מתחת לעטלפים. שלושים זוגות עיניים נפקחו בבת אחת.');
        g.sayAs('כיפה', 'אין מצב!! ...זה לא היה צרחה. זה היה... תיאום ציפיות עם העטלפים.');
      },
    },
  ],

  onCommand(g, verb, noun) {
    // Stalactites are scenery without a spec rect — handled here (looks.md #22).
    if (noun === 'נטיפים' || noun === 'נטיף') {
      if (verb === 'look') {
        g.say(
          'נטיפים תלויים מהתקרה כמו שיניים של מישהו שמתחמק מרופא מהתקופה הפליאוליתית. אחד מטפטף בקצב קבוע — טיק, טיק, טיק — כאילו הוא סופר אותך.'
        );
      } else if (verb === 'take') {
        g.say('הנטיף הזה גדל אלף שנה. את פה חמש דקות. כבדי את הוותק.');
      } else {
        g.say('הנטיפים עסוקים בלטפטף. עוד אלף שנה הם יהיו עמודים. סבלנות זה הקטע שלהם, לא שלך.');
      }
      return true;
    }
    // Shouting in a cave — one allowed light beat, the echo has opinions.
    if (verb === 'call' && !noun) {
      g.sayAs('כיפה', 'יש פה מישהו?!');
      g.say('"...מישהו... מישהו..." — ההד החזיר. ואז, נשבע, הוא הוסיף "שששש". גם להד יש אינסטינקטים.');
      return true;
    }
    return false;
  },

  exits: {
    // Land right below the cave mouth in scene08 (same spot as the torch-gate return).
    down: { to: 'scene08', x: 82, y: 132 },
  },
});
